x = 0:10^4;
Ab = binopdf(x, 10^4, 0.01);
Bb = binopdf(x, 10^4, 0.1);
Cb = binopdf(x, 10^4, 0.2);
Db = binopdf(x, 10^4, 0.5);
Ap = poisspdf(x, 0.01*10^4);
Bp = poisspdf(x, 0.1*10^4);
Cp = poisspdf(x, 0.2*10^4);
Dp = poisspdf(x, 0.5*10^4);
figure;
plot(x, Ab, x, Ap);title('0.01');
figure;
plot(x, Bb, x, Bp);title('0.1');
figure;
plot(x, Cb, x, Cp);title('0.2');
figure;
plot(x, Db, x, Dp);title('0.5');
figure;
plot(x, abs(Ab-Ap), x, abs(Bb-Bp), x, abs(Cb-Cp), x, abs(Db-Dp));
legend({'0.01','0.1','0.2','0.5'});